class ToolRequest(BaseModel):
    """
    Payload dispatched to the host runner to invoke a specific system-level capability.
    """
    request_id: UUID = Field(
        default_factory=uuid4,
        description="The unique global tracing identification token for this request."
    )
    task_id: UUID = Field(
        ...,
        description="Reference UUID of the parent AgentTask generating this request."
    )
    tool_name: str = Field(
        ...,
        min_length=1,
        pattern=r"^[a-zA-Z0-9_\-\.]+$",
        description="Namespace identifier of the tool in reverse-dns style or direct naming."
    )
    arguments: Dict[str, Any] = Field(
        default_factory=dict,
        description="Structural schema arguments required for tool execution."
    )
    requested_at: datetime = Field(
        default_factory=datetime.utcnow,
        description="UTC time tag denoting when the runtime dispatched this unit."
    )
    timeout_seconds: int = Field(
        default=300,
        ge=1,
        le=3600,
        description="Enforced runtime sandbox execution timeout threshold."
    )


class ToolResult(BaseModel):
    """
    Unified response enclosing output data, metrics, or errors emitted during tool run.
    """
    result_id: UUID = Field(
        default_factory=uuid4,
        description="The unique transaction ID for this tool execution event."
    )
    request_id: UUID = Field(
        ...,
        description="Corresponding request identification matching the originating ToolRequest."
    )
    status: ToolStatus = Field(
        ...,
        description="Terminal status representing the invocation lifecycle state."
    )
    output: Optional[Any] = Field(
        None,
        description="The structured output payload generated by the tool upon success."
    )
    error: Optional[str] = Field(
        None,
        description="Verbose string error details or stderr trace output if execution fails."
    )
    completed_at: datetime = Field(
        default_factory=datetime.utcnow,
        description="UTC time tag when execution terminated and results were gathered."
    )
    duration_ms: float = Field(
        ...,
        ge=0.0,
        description="Enforced execution telemetry showing exact runtime duration in milliseconds."
    )

    @model_validator(mode="after")
    def validate_terminal_fields(self) -> "ToolResult":
        """
        Enforce alignment of status, outputs, and errors inside result.
        """
        if self.status == ToolStatus.FAILURE and not self.error:
            raise ValueError("Tool execution failure requires an associated error message.")
        if self.status == ToolStatus.SUCCESS and self.error:
            raise ValueError("Successful Tool run cannot contain an error payload.")
        return self


