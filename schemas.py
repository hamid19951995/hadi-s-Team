from datetime import datetime
from enum import Enum
from typing import Any, Dict, List, Optional, Union
from uuid import UUID, uuid4
from pydantic import BaseModel, Field, field_validator, model_validator


# ==========================================
# 1. State & Lifecycle Enumerations
# ==========================================

class TaskStatus(str, Enum):
    """
    Represents the operational lifecycle states of an AgentTask.
    
    Transitions:
        PENDING -> QUEUED -> RUNNING -> COMPLETED
                                    -> FAILED
                                    -> CANCELLED
                 -> BLOCKED -> RUNNING
    """
    PENDING = "pending"
    QUEUED = "queued"
    RUNNING = "running"
    BLOCKED = "blocked"
    COMPLETED = "completed"
    FAILED = "failed"
    CANCELLED = "cancelled"


class ToolStatus(str, Enum):
    """
    Represents the lifecycle transition states of a discrete Tool invocation.
    
    Transitions:
        REQUESTED -> EXECUTING -> SUCCESS
                               -> FAILURE
    """
    REQUESTED = "requested"
    EXECUTING = "executing"
    SUCCESS = "success"
    FAILURE = "failure"


class NodeStatus(str, Enum):
    """
    Lifecycle tracking states of a TaskNode within the execution DAG.
    
    Transitions:
        IDLE -> RUNNING -> COMPLETED
                        -> FAILED
             -> SKIPPED (due to parent failure and lack of recovery path)
    """
    IDLE = "idle"
    RUNNING = "running"
    COMPLETED = "completed"
    FAILED = "failed"
    SKIPPED = "skipped"


class MemoryType(str, Enum):
    """
    System Memory partitions utilizing distinct retrieval heuristics and decay curves.
    """
    EPISODIC = "episodic"      # Specific execution footsteps, traces, and tool logs
    SEMANTIC = "semantic"      # Facts, structural requirements, domain knowledge, codebase facts
    PROCEDURAL = "procedural"  # Workflow definitions, step plans, tool schemas, best practices
    WORKING = "working"        # Active execution thread state, current context window buffers


class EventSeverity(str, Enum):
    """
    System event severity grading for routing, auditing, and log metrics.
    """
    DEBUG = "debug"
    INFO = "info"
    WARNING = "warning"
    ERROR = "error"
    CRITICAL = "critical"


